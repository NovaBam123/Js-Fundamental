/*  
    BERFUNGSI UNTUK MEMANGKAS SPACE/SPASI PADA KIEDUA UJUNG STRING
    tidak merubah string asli 
    SYNTAX:
    - trim()
    - trimStart() = pangkas spasi awal
    - trimEnd() = pangakas spasi akhir
*/
const str = "  nova bamahry   "
console.log(str)
console.log(str.trim());
console.log(str.trimStart());
console.log(str.trimEnd());
